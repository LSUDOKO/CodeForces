#include<iostream>
using namespace std;
int main(){
    cout<<"Hello World!"<<endl;
    // Add multiple lines 
    cout<<"This is a C++ program.";
    cout<<"my name is arpit";//both will be printed in same line
    // Add a new line
    cout<<"hello\n";
    cout<<"arpit\n";
    // Cascading Effect
    cout<<"This is a C++ program."<<endl<<"my name is arpit"<<endl;
    return 0;
}